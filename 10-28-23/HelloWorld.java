// File names should end with .java so that it could be recognized by the compiler
// Class name should be the same with the file name
public class HelloWorld {

    // This is the main method of our program
    public static void main(String[] args) {

        // Printing hello world
        System.out.println("hello world"); // prints hello world to our console

        // Printing hello world on two different lines
        System.out.println("hello");
        System.out.println("world");
    }

}
