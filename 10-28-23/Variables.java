public class Variables {

    public static void main(String[] args) {

        // Creating a String variable that will contain the value "Angelo"
        // A String is a data type that stores words
        String name = "Angelo";
        // An int is a data type that stores whole numbers
        int num1 = 23;
        int num2 = 11;

        // Printing our name variables
        System.out.println(name); // to print the value of a variable, we put the variable name in the parentheses
        System.out.println(num1);
        System.out.println(num2);
        // This will display
        // Angelo
        // 23
        // 11
        // in our console

    }

}
