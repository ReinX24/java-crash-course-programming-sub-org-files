public class WhileLoop {

    public static void main(String[] args) {

        // This variable will serve as our counter
        int index = 0;

        // This while loop will run until the index reaches to 10
        // This loop will print the message "Hello" 10 times
        while (index < 10) {
            System.out.println("Hello");
            index = index + 1; // this will increase the value of index by 1
        }

    }

}